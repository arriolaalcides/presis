<?php

namespace Presis\MovistarBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * TipoCliente
 *
 * @ORM\Table()
 * @ORM\Entity(repositoryClass="Presis\MovistarBundle\Entity\TipoClienteRepository")
 */
class TipoClienteMovistar
{
    /**
     * @var integer
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="descripcion", type="string", length=45)
     */
    private $descripcion;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set descripcion
     *
     * @param string $descripcion
     *
     * @return TipoClienteMovistar
     */
    public function setDescripcion($descripcion)
    {
        $this->descripcion = $descripcion;

        return $this;
    }

    /**
     * Get descripcion
     *
     * @return string
     */
    public function getDescripcion()
    {
        return $this->descripcion;
    }

    public function __toString()
    {
        return $this->descripcion;
    }
}

